-- =============================================================================
-- Phase 12 — Invoicing integrations foundation
--
-- Adds the DB plumbing for a real invoicing flow:
--   1. `invoice_payments` — one row per payment against an invoice. Supports
--      partial payments and every payment method from "cash" to a processor
--      webhook row. A trigger keeps `invoices.status` + `invoices.paid_at` in
--      sync with `sum(invoice_payments.amount_cents)`, so manual record-payment
--      actions and processor webhooks both write to the same place with
--      identical downstream behaviour.
--   2. `integration_connections` — one row per (org, provider) OAuth link for
--      Stripe Connect, Square, and QuickBooks Online. Tokens are stored
--      encrypted at the application layer (AES-256-GCM via src/lib/crypto.ts)
--      so leaking the DB alone does not leak capability tokens.
--   3. `integration_events` — append-only log of inbound webhooks for
--      idempotency (dedupe on `event_id`) and debugging.
--   4. `invoices.number` — sequential human-readable invoice number per org
--      (e.g. INV-2026-0001) generated by a BEFORE INSERT trigger.
--   5. `invoices.public_token` — 16-char URL-safe capability token for the
--      public `/i/:token` no-login invoice view (same pattern as /claim/:token).
--   6. `clients.quickbooks_customer_id` — nullable cache of the QBO customer
--      id once a client has been mirrored into the user's QuickBooks ledger.
--   7. Audit log immutability — a trigger on `public.audit_log` that raises
--      on any UPDATE or DELETE. RLS already denies these at the app role
--      level, but this locks it down even for the service role, so no code
--      path (intentional or otherwise) can rewrite history.
-- =============================================================================

