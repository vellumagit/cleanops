-- Bump the invoice_status enum to cover partial payments. Postgres can't
-- ALTER a type inside a transaction with other statements in Supabase's
-- migration runner, so we inline the ADD VALUE into its own DO block.
do $$
begin
  if not exists (
    select 1 from pg_enum
    where enumlabel = 'partially_paid'
      and enumtypid = 'public.invoice_status'::regtype
  ) then
    alter type public.invoice_status add value 'partially_paid' before 'paid';
  end if;
end $$;

do $$
begin
  if not exists (
    select 1 from pg_enum
    where enumlabel = 'void'
      and enumtypid = 'public.invoice_status'::regtype
  ) then
    alter type public.invoice_status add value 'void';
  end if;
end $$;

