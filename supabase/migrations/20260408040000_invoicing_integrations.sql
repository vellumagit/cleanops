-- Keep invoices.status + paid_at in sync with sum of invoice_payments.
-- A single function handles insert/update/delete on invoice_payments.
create or replace function public.sync_invoice_payment_totals()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_invoice_id uuid;
  v_total      integer;
  v_paid       integer;
  v_status     public.invoice_status;
  v_paid_at    timestamptz;
  v_due_date   date;
  v_voided_at  timestamptz;
begin
  -- Figure out which invoice row this change touched.
  v_invoice_id := coalesce(new.invoice_id, old.invoice_id);
  if v_invoice_id is null then
    return coalesce(new, old);
  end if;

  select amount_cents, due_date, voided_at
    into v_total, v_due_date, v_voided_at
    from public.invoices
   where id = v_invoice_id;

  if not found then
    return coalesce(new, old);
  end if;

  select coalesce(sum(amount_cents), 0)
    into v_paid
    from public.invoice_payments
   where invoice_id = v_invoice_id;

  -- Void trumps everything.
  if v_voided_at is not null then
    v_status := 'void';
    v_paid_at := null;
  elsif v_paid >= v_total and v_total > 0 then
    v_status := 'paid';
    select max(received_at) into v_paid_at
      from public.invoice_payments
     where invoice_id = v_invoice_id;
  elsif v_paid > 0 then
    v_status := 'partially_paid';
    v_paid_at := null;
  else
    -- No payments. Fall back to sent / overdue / draft based on dates.
    if v_due_date is not null and v_due_date < current_date then
      v_status := 'overdue';
    else
      -- Leave status alone if it's already draft or sent so the admin's
      -- "send invoice" action controls the draft→sent transition. We only
      -- rewrite to overdue/paid/partially_paid/void from here.
      select status into v_status
        from public.invoices
       where id = v_invoice_id;
    end if;
    v_paid_at := null;
  end if;

  update public.invoices
     set status = v_status,
         paid_at = v_paid_at,
         updated_at = now()
   where id = v_invoice_id;

  return coalesce(new, old);
end;
$$;

drop trigger if exists invoice_payments_sync_totals on public.invoice_payments;
create trigger invoice_payments_sync_totals
  after insert or update or delete on public.invoice_payments
  for each row execute function public.sync_invoice_payment_totals();

-- ---- Invoice number generator -----------------------------------------------
-- Per-org sequential number, format INV-YYYY-NNNN. Allocated at INSERT time
-- inside a transaction so concurrent inserts can't collide. We look up the
-- current max for the org/year and add one; the unique index on
-- (organization_id, number) is the final race guard.
create or replace function public.assign_invoice_number()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_year int := extract(year from now())::int;
  v_prefix text := 'INV-' || v_year::text || '-';
  v_next int;
begin
  if new.number is not null and new.number <> '' then
    return new;
  end if;

  select coalesce(
           max(
             nullif(
               regexp_replace(number, '^' || v_prefix, ''),
               ''
             )::int
           ),
           0
         ) + 1
    into v_next
    from public.invoices
   where organization_id = new.organization_id
     and number like v_prefix || '%';

  new.number := v_prefix || lpad(v_next::text, 4, '0');
  return new;
end;
$$;

drop trigger if exists invoices_assign_number on public.invoices;
create trigger invoices_assign_number
  before insert on public.invoices
  for each row execute function public.assign_invoice_number();

-- ---- Public token generator -------------------------------------------------
create or replace function public.assign_invoice_public_token()
returns trigger
language plpgsql
security definer
set search_path = public, extensions
as $$
begin
  if new.public_token is null then
    -- 12 random bytes → 16 base64 chars (url-safe). pgcrypto.gen_random_bytes
    -- is available under extensions schema in Supabase.
    new.public_token := replace(
      replace(
        replace(
          encode(extensions.gen_random_bytes(12), 'base64'),
          '+', '-'
        ),
        '/', '_'
      ),
      '=', ''
    );
  end if;
  return new;
end;
$$;

drop trigger if exists invoices_assign_public_token on public.invoices;
create trigger invoices_assign_public_token
  before insert on public.invoices
  for each row execute function public.assign_invoice_public_token();

-- Backfill tokens + numbers for any invoices that predate this migration.
do $$
declare
  r record;
  v_prefix text;
  v_next int;
  v_token text;
begin
  for r in
    select id, organization_id, created_at, number, public_token
      from public.invoices
     where number is null or public_token is null
     order by organization_id, created_at
  loop
    if r.number is null then
      v_prefix := 'INV-' || extract(year from r.created_at)::int::text || '-';
      select coalesce(
               max(
                 nullif(regexp_replace(number, '^' || v_prefix, ''), '')::int
               ),
               0
             ) + 1
        into v_next
        from public.invoices
       where organization_id = r.organization_id
         and number like v_prefix || '%';
      update public.invoices
         set number = v_prefix || lpad(v_next::text, 4, '0')
       where id = r.id;
    end if;

    if r.public_token is null then
      v_token := replace(
        replace(
          replace(
            encode(extensions.gen_random_bytes(12), 'base64'),
            '+', '-'
          ),
          '/', '_'
        ),
        '=', ''
      );
      update public.invoices
         set public_token = v_token
       where id = r.id;
    end if;
  end loop;
end $$;

-- ---- Audit log immutability -------------------------------------------------
-- RLS already denies update/delete at the authenticated role level, but
-- a service-role-powered code path (or a buggy migration) could still
-- rewrite history. This trigger closes that loophole.
create or replace function public.audit_log_reject_mutations()
returns trigger
language plpgsql
as $$
begin
  raise exception 'audit_log is append-only (attempted %)', tg_op
    using errcode = 'insufficient_privilege';
end;
$$;

drop trigger if exists audit_log_block_update on public.audit_log;
create trigger audit_log_block_update
  before update on public.audit_log
  for each row execute function public.audit_log_reject_mutations();

drop trigger if exists audit_log_block_delete on public.audit_log;
create trigger audit_log_block_delete
  before delete on public.audit_log
  for each row execute function public.audit_log_reject_mutations();

